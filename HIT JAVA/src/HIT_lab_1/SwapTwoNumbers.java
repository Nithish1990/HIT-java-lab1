package HIT_lab_1;

public class SwapTwoNumbers {
	public static void main (String []args) {
		int num1 = 10 , num2 = 4,temp;
		System.out.println("Before swaping ");
		System.out.println("Number 1 = "+num1+", Number 2 = "+num2);
		temp = num1;
		num1 = num2;
		num2 = temp;
		System.out.println("After swaping ");
		System.out.println("Number 1 = "+num1+", Number 2 = "+num2);
		
		
	}

}
